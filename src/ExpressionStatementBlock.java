public class ExpressionStatementBlock {
    public static void main(String[] args) {
        // Block -> diawali dengan "{" dan diakhiri dengan "}"

        // Expression & Statement
        int age;
        age = 22; // <- Expression
        System.out.println(age); // <- Statement
        System.out.println(age = 23); // <- Statement, namun dibagian "age = 23" merupakan expression

    }
}
