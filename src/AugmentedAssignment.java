public class AugmentedAssignment {
    public static void main(String[] args) {
        // Operasi Matematika
        int a = 10;
        int b = 10;
        int result = a + b;
        System.out.println(result);

        // Augmented Assignment
        int c = 10;
        c += 10;
        System.out.println(c);
    }
}
