public class OperasiPerbadingan {
    public static void main(String[] args) {
        // > Lebih Dari
        // < Kurang Dari
        // >= Lebih Dari Sama Dengan
        // <= Kurang Dari Sama Dengan
        // == Sama Dengan
        // != Tidak Sama Dengan

        int a = 100;
        int b = 100;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);

    }
}
