public class Variabel {
    public static void main(String[] args) {
        // Inisialisasi variabel
        String name;
        // Input value
        name = "Shufi";
        // Print variabel to Console
        System.out.println(name);

        // Inisialisasi variabel + input value
        int age = 22;
        // Print variabel to Console
        System.out.println(age);

        // Inisialisasi variabel + input value Java10++ keyword : var
        var newName = "Shufi New";
        System.out.println(newName);

        // Final var
        final int newAge = 23;
        System.out.println(newAge);
    }
}
